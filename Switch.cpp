#include<iostream>
using namespace std;
int main() {
    int day=4;
    switch(day) {
        case 1:
        cout << "Monday\n";
        break;
        case 2:
        cout << "Tuesday\n";
        break;
        case 3:
        cout << "Wednesday\n";
        break;
        case 4:
        cout << "Thursday\n";
        case 5:
        cout << "Friday\n";
        case 6:
        cout << "Saturday\n";
        case 7:
        cout << "Sunday\n";
    }
    return 0;
}